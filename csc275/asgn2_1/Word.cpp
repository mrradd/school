
#include "Word.h"

/*******************************************************************************
* CTOR */
/**
*******************************************************************************/
Word::Word(std::string word)
  {
  mString = word;
  mCount  = 0;
  }

/*******************************************************************************
* DTOR */
/**
*******************************************************************************/
Word::~Word(){}

/*******************************************************************************
* SETTER/GETTERS
*******************************************************************************/  
void         Word::setString(std::string str) { mString = str;  }
std::string& Word::setString()                { return mString; }
int          Word::getCount ()                { return mCount;  }

/*******************************************************************************
* DTOR */
/**
*******************************************************************************/
void Word::incrementCount() { mCount++; }