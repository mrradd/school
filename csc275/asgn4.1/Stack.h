/*******************************************************************************
* Stack */
/**
* Emulates the functionality of an STL Stack.
*******************************************************************************/
#include <iostream>
#include <stdexcept>

template <class T, int MaxStack>
class Stack
  {
  /*****************************************************************************
  * Datamembers
  *****************************************************************************/
  private:
    int mEmptyVal;
    T   mList[MaxStack];
    int mTop;
    
  /*****************************************************************************
  * Enums
  *****************************************************************************/
  /*****************************************************************************
  * CTORS/DTORS
  *****************************************************************************/
  public:
    Stack();
    ~Stack();

  /*****************************************************************************
  * Accessors
  *****************************************************************************/ 
  /*****************************************************************************
  * Methods
  *****************************************************************************/
  void push (T t);
  void pop  ();
  T    top  ();
  bool empty();

  };